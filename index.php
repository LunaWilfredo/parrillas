    <h1><strong>¿Que combustible es mejor : <i>carbón</i>,<i>gas</i> o <i>madera</i> ?</strong></h1>
    <p class="lead">Te recomendamos la lectura de este artículo: <a href="#">Cuál es el mejor carbón para parrillas.</a></p>
    <img src="img/carbon.jpg" class="card-img" alt="Responsive image">
    <p class="lead"><b>Cada combustible tiene sus ventajas e inconvenientes.</b>Por ejemplo, el gas butano tiene la ventaja de ser cómodo y fácil de utilizar, tanto en su encendido como en el control de la llama.</p>
    <p>Sin embargo, los auténticos amantes de la barbacoa prefieren la leña o el carbón. Hay muchos tipos de maderas y carbón que podemos utilizar para nuestras parrilladas, y en muchos casos, determinarán el sabor y aroma que van a adquirir los alimentos.</p>
    <p>Entre las maderas más utilizadas para cocinar en parrillas encontramos la leña de encina, de olivo, de roble, de naranjo, de haya y de fresno, entre otras.</p>
    <i><b>¡Atención! Maderas Tóxicas para cocinar en parrillas o barbacoas.</b></i>
    <p>Esto es algo que no todo el mundo sabe y que puede convertir una tarde de alegría en una pesadilla de jaquecas y dolor de cabeza.<b>Hay maderas que NUNCA deben utilizarse para cocinar alimentos</b>, entre estas destacan la leña de higuera, así como madera tratada con pinturas o barnices, ya que la combustión de estos productos químicos puede ser peligrosa para nuestra salud.</p>
    <p>El carbón vegetal tiene muchas variantes <b>según la madera de procedencia, así como su proceso de carbonización</b>, podemos encontrar carbones de quebracho blanco, de fibra de coco, de encina, entre muchos otros.</p>
